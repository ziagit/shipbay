        $result=0;
        foreach ($request->items as $item) {
            $x = (($item['length'] * $item['width'] * $item['height']) / 166) * $item['number'];
            $y = $item['weight'] * $item['number'];
            $xy = $x >= $y ? $x : $y;
            $result = $result + $xy;
        }
        return $result;
    }
